#include "ft_ls.h"

int		ft_get_hide(char **files)
{
	int i;

	i = 0;
	while (files[i])
	{
		if (files[i][0] != '\0' && files[i][0] != '.')
			return (1);
		i++;
	}
	return (0);
}
